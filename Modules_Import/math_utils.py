# Q. MAKE A FILE MATH_UTILS WITH BASIC MATHEMATIC FUNCTIONS, THEN IMPORT THIS FILE TO TEST.PY AND USE IT THERE.

def add(x,y):
    return x + y

def sub(x,y):
    return x - y

def mul(x,y):
    return x * y

def div(x,y):
    return x / y

def square(x):
    return x * x

def cube(x):
    return x * x * x